import java.io.Serializable;

/**
 * pk fuer eine policy
 * 
 * @author geidell
 * 
 */
public class PolicyKeyDTO implements Serializable {

	private static final long serialVersionUID = 4680960399240307137L;
	private String domainName;
	private String name;
	private String version;

	public PolicyKeyDTO() {
	}

	public PolicyKeyDTO(String domainName, String name, String version) {
		super();
		this.domainName = domainName;
		this.name = name;
		this.version = version;
	}

	public String getDomainName() {
		return domainName;
	}

	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((domainName == null) ? 0 : domainName.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((version == null) ? 0 : version.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PolicyKeyDTO other = (PolicyKeyDTO) obj;
		if (domainName == null) {
			if (other.domainName != null)
				return false;
		} else if (!domainName.equals(other.domainName))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (version == null) {
			if (other.version != null)
				return false;
		} else if (!version.equals(other.version))
			return false;
		return true;
	}

	@Override
	public String toString() {
		final StringBuilder sb = new StringBuilder();
		sb.append("policy with name '");
		sb.append(name);
		sb.append("' for domain '");
		sb.append(domainName);
		sb.append("' in version ");
		sb.append(version);
		return sb.toString();
	}
}
