import java.io.Serializable;

import javax.persistence.EmbeddedId;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.MapsId;
import javax.persistence.Table;

import org.eclipse.persistence.annotations.Cache;
import org.eclipse.persistence.config.CacheIsolationType;

/**
 * type fuer die signer id
 * 
 * @author geidell
 * 
 */
@Entity
@Table(name = "signer_id_type")
@Cache(isolation = CacheIsolationType.PROTECTED)
public class SignerIdType implements Serializable {

	private static final long serialVersionUID = -6846427145778814076L;
	@EmbeddedId
	private SignerIdTypeKey key;
	@ManyToOne(fetch = FetchType.EAGER)
	@JoinColumn(name = "DOMAIN_NAME", referencedColumnName = "NAME")
	@MapsId("domainName")
	private Domain domain;

	// keine explizite relation zu SignerId

	public SignerIdType() {
	}

	public SignerIdType(Domain domain, String name) {
		this.key = new SignerIdTypeKey(domain.getName(), name);
		this.domain = domain;
	}

	public SignerIdTypeKey getKey() {
		return key;
	}

	public Domain getDomain() {
		return domain;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SignerIdType other = (SignerIdType) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return key.toString();
	}
}
