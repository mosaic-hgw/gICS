import java.io.Serializable;

/**
 * eine policy ist die kleinstmoegliche unterteilung eines consents; sie repraesentiert eine atomare, zustimmbare einheit
 * 
 * @author geidell
 * 
 */
public class PolicyDTO implements Serializable {

	private static final long serialVersionUID = -98288145960857096L;
	private PolicyKeyDTO key;
	private String comment;
	private String externProperties;

	public PolicyDTO() {
	}

	public PolicyDTO(PolicyKeyDTO key) {
		super();
		this.key = key;
	}

	public PolicyDTO(PolicyKeyDTO key, String comment, String externProperties) {
		super();
		this.key = key;
		this.comment = comment;
		this.externProperties = externProperties;
	}

	public PolicyKeyDTO getKey() {
		return key;
	}

	public void setKey(PolicyKeyDTO key) {
		if (key != null) {
			this.key = key;
		}
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getExternProperties() {
		return externProperties;
	}

	public void setExternProperties(String externProperties) {
		this.externProperties = externProperties;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((comment == null) ? 0 : comment.hashCode());
		result = prime * result + ((externProperties == null) ? 0 : externProperties.hashCode());
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PolicyDTO other = (PolicyDTO) obj;
		if (comment == null) {
			if (other.comment != null)
				return false;
		} else if (!comment.equals(other.comment))
			return false;
		if (externProperties == null) {
			if (other.externProperties != null)
				return false;
		} else if (!externProperties.equals(other.externProperties))
			return false;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return (key == null) ? "POLICY DTO WITH NULL AS KEY" : key.toString();
	}
}
